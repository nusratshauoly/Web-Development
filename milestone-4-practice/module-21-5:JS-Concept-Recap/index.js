//2. Array concat()
/*
concat()  //returns a new array containing the value of the concatenated arrays.

const array1 = [10, 20, 30, 40];
const array2 = ['a', 'b', 'c', 'd'];

const newArray = array1.concat(array2);
newArray = [10, 20, 30, 40, 'a', 'b', 'c', 'd'];
*/




